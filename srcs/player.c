#include "filler.h"

int			read_player(t_filler *fil, int n)
{
	if (n != 1 && n != 2)
		return (1);
	fil->player = n == 1 ? 'O' : 'X';
	fil->opp = n == 1 ? 'X' : 'O';
	return (0);
}
